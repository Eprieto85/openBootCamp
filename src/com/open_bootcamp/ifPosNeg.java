package com.open_bootcamp;

public class ifPosNeg {
    public static void main(String[] args) {
        int num=-1;

        if(num==0){
            System.out.println("Es cero");
        }else if(num>0){
            System.out.println("Es positivo");
        }else{
            System.out.println("Es negativo");
        }
    }
}
